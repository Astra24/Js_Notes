// program to display text 5 times
const a = 5;
for (let i = 1; i <= a; i++) {
    console.log(`I love me.`);
}

// program to display numbers from 1 to 5
let b = 1, c = 5;
while (b <= c) {
    console.log(b);
    b += 1;
}

